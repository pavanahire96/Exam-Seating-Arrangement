    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed
        // TODO add your handling code here:
        String roomNo=tf1.getText();
        String roomSize=cb1.getSelectedItem().toString();
        String floorNo=cb2.getSelectedItem().toString();
                    
                    if(verifyFields()){
                        PreparedStatement ps;
                ResultSet rs;
                
                String query="INSERT INTO `room`(`roomNo`,`roomSize`, `floorNo`) VALUES (?,?,?)";
                    try {
                        ps= (PreparedStatement) connection.getConnection().prepareStatement(query);
                        ps.setString(1,roomNo);
                        ps.setString(2,roomSize);
                        ps.setString(3,floorNo);
                        
                        
                        if(ps.executeUpdate()!=0){
                        JOptionPane.showMessageDialog(null,"Data has been Inserted Successfully","Updating Data",2);
                        }else{
                        JOptionPane.showMessageDialog(null,"Failed to update data","Failed!!",2);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Room.class.getName()).log(Level.SEVERE, null, ex);
                    }
                
                    }
    }//GEN-LAST:event_addActionPerformed

