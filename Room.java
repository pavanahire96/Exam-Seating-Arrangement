    public ArrayList<Rooms> userList(){
    ArrayList<Rooms> studentsList=new ArrayList<>();
    PreparedStatement st;
            ResultSet rs;
                      
            
            String query="SELECT * FROM room";
            try {
                st=(PreparedStatement) connection.getConnection().prepareStatement(query);
                
                Rooms s;
            
            rs= (ResultSet) st.executeQuery();
            
            while(rs.next()){
            
                s=new Rooms(rs.getString("roomNo"),rs.getString("roomSize"),rs.getString("floorNo"));
                studentsList.add(s);
            }
            
            } catch (SQLException ex) {
                Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
            }
            return studentsList;
    }

    public void show_rooms(){
    ArrayList<Rooms> list=userList();
    DefaultTableModel model=(DefaultTableModel) jTable_Display_Rooms.getModel();
    Object[] row=new Object[3];
    for(int i=0;i<list.size();i++){
    row[0]=list.get(i).getRoomNo();
    row[1]=list.get(i).getRoomSize();
    row[2]=list.get(i).getFloorNo();
    
    model.addRow(row);
    }
    }
    
